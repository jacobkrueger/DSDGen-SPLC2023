package utils;

/**
 * Contains file extensions used in this program.
 * 
 * @author Alex Mikulinski
 * @since Jan 31, 2019
 */
public interface FileExtension {
	/**
	 * Extension for JSON files.
	 */
	String	JSON	= ".json";

	/**
	 * Extension for CSV files.
	 */
	String	CSV	= ".csv";

	/**
	 * Extension for HTML files.
	 */
	String	HTML	= ".html";

	/**
	 * Extension for java files.
	 */
	String	JAVA	= ".java";

	/**
	 * Extension for a jar.
	 */
	String	JAR	= ".jar";
}
